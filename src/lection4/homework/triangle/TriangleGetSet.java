package lection4.homework.triangle;

public class TriangleGetSet {
    private int length;
    private int width;


    public TriangleGetSet(int length, int width) {
        this.length = length;
        this.width = width;

    }

    public int getLength() {
        return length;
    }

    public void setLength(int length) {
        this.length = length;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

}
